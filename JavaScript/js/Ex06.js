var n1, n2, soma, multiplicacao, subtracao, divisao

n1=prompt("Digite o primeiro valor em reais: ")
alert(n1)
n2=prompt("Digite o outro valor em reais: ")

soma= parseFloat(n1)+parseFloat(n2) 
alert("A soma dos númmeros reais é: " + soma)

subtracao= parseFloat(n1)-parseFloat(n2)
alert("A subtração dos números reais é: " + subtracao)

multiplicacao= parseFloat(n1) * parseFloat(n2)
alert("A multiplicação dos números reais é: " + multiplicacao)

divisao= parseFloat(n1) / parseFloat(n2)
alert("A divisão dos números reais é: " + divisao)