var dia

dia=prompt("Digite o número correspondente ao dia da semana: ")


switch (dia) {
    case "1":
        {
            alert("Domingo")
            break;
        }
    case "2":
        {
            alert("Segunda-feira")
             break;
        }
    case "3":
        {
            alert("Terça-feira")
            break;
        }
    case "4":
        {
            alert("Quarta-feira")
            break;
        }
                
    default:
        break;
}